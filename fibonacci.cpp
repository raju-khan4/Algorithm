#include <iostream>
using namespace std;

int fibonacci(int n) {
    if (n == 0)
        return 0;       // Base case 1
    else if (n == 1)
        return 1;       // Base case 2
    else
        return fibonacci(n - 1) + fibonacci(n - 2); // Recursive call
}

int main() {
    int n;
    cout << "Enter number of terms: ";
    cin >> n;

    cout << "Fibonacci Series: ";
    for (int i = 0; i < n; i++) {
        cout << fibonacci(i) << " ";
    }
    return 0;
}
